package com.jokingsun.oilfairy.widget.manager.logevent.model;

public class ReqDateEventModel {
    private String dateId;
    private String dateSituation;
    private String dateCharge;
    private String dateCategory;

    public String getDateId() {
        return dateId;
    }

    public void setDateId(String dateId) {
        this.dateId = dateId;
    }

    public String getDateSituation() {
        return dateSituation;
    }

    public void setDateSituation(String dateSituation) {
        this.dateSituation = dateSituation;
    }

    public String getDateCharge() {
        return dateCharge;
    }

    public void setDateCharge(String dateCharge) {
        this.dateCharge = dateCharge;
    }

    public String getDateCategory() {
        return dateCategory;
    }

    public void setDateCategory(String dateCategory) {
        this.dateCategory = dateCategory;
    }
}
